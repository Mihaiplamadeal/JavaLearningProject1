package md.tekwillacademy.operationandcyclicfunctions;

public class LogicalOperations {

    public static void main(String[] args) {
        boolean a = true;
        boolean b = false;
        boolean c = a && b;

        System.out.println(c);
        System.out.println(a);

        System.out.println(a || b);
    }
}
